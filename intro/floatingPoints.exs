# Floating-point numbers are written using a decimal point.
# There must be at least one digit before and after the decimal point.
# An optional trailing exponent may be given

# All of these are valid floating point numbers

# Floats are IEEE 754 double precision, giving them about 16 digits of accuracy
# and a maximum exponent of around 10308

num1 = 1.0
num2 = 0.23456
num3 = 3.14159e1
num4 = 314_923.014e5


IO.puts(num1)
IO.puts(num2)
IO.puts(num3)
